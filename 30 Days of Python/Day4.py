# Function with argument
def greet(name):
    print(f"Hello, {name}")
    print("Good Morning")
    print(f"How do you do {name}?")
greet("RB")

# Position argument-> (name,location) order matters
# keyword arguments -> (location="india", name="RB") order does not matter